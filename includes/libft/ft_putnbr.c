#include "libft.h"

void	ft_putnbr(int n)
{
	long long	m;

	m = (long long)n;
	if (m < 0)
	{
		ft_putchar('-');
		m *= (-1);
	}
	if (m > 9)
		ft_putnbr(m / 10);
	ft_putchar(m % 10 + '0');
}
